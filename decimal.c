#include<stdio.h>
int main(){
    int a=18.89; //int means number
     char str[]="srujan"; //string
      char c='9'; //charcter

    printf("%d\n",a);//decimal
    printf("%o\n",a);//octal
    printf("%x\n",a);//lower case
    printf("%X\n",a);//upper case
    printf("%f\n",a);//float
    printf("%s\n",str);//string
    printf("%c\n",c);//charcter
    return 0;
}